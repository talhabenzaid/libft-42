#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*d;

	i = 0;
	d = malloc ((ft_strlen(s1) + 1) * sizeof(char));
	if (!d)
		return (NULL);
	while (s1[i])
	{
		d[i] = s1[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}
